// Funci√≥n para verificar y corregir rutas de im√°genes
function fixImagePaths() {
    if (!window.quizQuestions) return;
    
    console.log("üñºÔ∏è Verificando rutas de im√°genes en preguntas...");
    
    window.quizQuestions.forEach((question, index) => {
        // Si la pregunta tiene una imagen
        if (question.image) {
            // Si la ruta comienza con "path/to/"
            if (question.image.startsWith('path/to/')) {
                // Extraer el nombre del archivo
                const fileName = question.image.split('/').pop();
                
                // Crear una ruta correcta (asumiendo que est√°n en img/quiz/)
                const correctPath = `img/quiz/${fileName}`;
                
                console.log(`üîÑ Corrigiendo ruta de imagen en pregunta ${index+1}: 
                    ${question.image} -> ${correctPath}`);
                
                // Actualizar la ruta
                question.image = correctPath;
            }
            
            // Verificar si la imagen existe (solo como informaci√≥n)
            const img = new Image();
            img.onload = () => console.log(`‚úÖ Imagen encontrada: ${question.image}`);
            img.onerror = () => console.warn(`‚ö†Ô∏è Imagen no encontrada: ${question.image}`);
            img.src = question.image;
        }
        
        // Lo mismo para archivos de audio
        if (question.audio && question.audio.startsWith('path/to/')) {
            const fileName = question.audio.split('/').pop();
            const correctPath = `audio/quiz/${fileName}`;
            
            console.log(`üîÑ Corrigiendo ruta de audio en pregunta ${index+1}: 
                ${question.audio} -> ${correctPath}`);
            
            question.audio = correctPath;
        }
    });
    
    console.log("‚úÖ Verificaci√≥n de rutas completada");
}

// Ejecutar la correcci√≥n de rutas cuando el DOM est√© listo
document.addEventListener('DOMContentLoaded', fixImagePaths);

